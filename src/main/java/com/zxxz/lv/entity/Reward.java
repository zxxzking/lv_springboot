package com.zxxz.lv.entity;

public class Reward {

    String rewardName;

    String rewardType;

    String rewardProbability;

    String couponId;


    public String getRewardName() {
        return rewardName;
    }

    public void setRewardName(String rewardName) {
        this.rewardName = rewardName;
    }

    public String getRewardType() {
        return rewardType;
    }

    public void setRewardType(String rewardType) {
        this.rewardType = rewardType;
    }

    public String getRewardProbability() {
        return rewardProbability;
    }

    public void setRewardProbability(String rewardProbability) {
        this.rewardProbability = rewardProbability;
    }

    public String getCouponId() {
        return couponId;
    }

    public void setCouponId(String couponId) {
        this.couponId = couponId;
    }
}
